# Valores aleatorios con la funcion randint

# Import random
from random import randint

# Generar un numeero aleatorio entre 1 y 10
numrandom = randint(1, 10)
print(f" Numero aleatorio entre 1 y 10: {numrandom}")

# Simular un dado de seis caras
dado = randint(1, 6)
print(f" Resultado de lanzar el dado: {dado}")
