# Manejo de indices en una cadena


cadena1 = "Hola Mundo python"
print(cadena1)

# Recuperar el primer caracter

primer_caracter = cadena1[0] #Recupera H
print(primer_caracter)

# Recuperar el ultimo caracter
ultimo_caracter = cadena1[16]
print(ultimo_caracter)

ultimo_caracter = cadena1[len(cadena1)-1]
print(ultimo_caracter)

ultimo_caracter = cadena1[-1]
print(ultimo_caracter)

#Imprimir longitud cadena
longitud_cadena = len(cadena1)
print(longitud_cadena)

